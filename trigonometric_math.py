import math
def cal_sin(num):
    radian = math.radians(num)
    result = math.sin(radian)
    return result

def cal_cos(num):
    radian = math.radians(num)
    result = math.cos(radian)
    return result

def cal_tan(num):
    radian = math.radians(num)
    result = math.tan(radian)
    return result

def cal_asin(num):
    radian = math.asin(num)
    result = math.degrees(radian)
    return result

def cal_acos(num):
    radian = math.acos(num)
    result = math.degrees(radian)
    return result

def cal_atan(num):
    radian = math.atan(num)
    result = math.degrees(radian)
    return result